import UIKit

class NERoleType: NSObject,NEAttribute {

    func shangshi() {
        print("我是一名战士")
    }
    
    func fashi() {
        print("我是一名法师")
    }
    
    func chike() {
        print("我是一名刺客")
    }
    
    
}
